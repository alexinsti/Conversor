    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here:
        
        switch((String)jComboBoxMedidas.getSelectedItem()){
            case"Km/Millas":
                jTextField2.setText(Double.toString(Conversor.kmAMillas(Integer.parseInt(jTextField1.getText()))));
                break;
            
            case"L/Galones":
                jTextField2.setText(Double.toString(Conversor.lAGalones(Integer.parseInt(jTextField1.getText()))));
                break;
                
            case"Dólares/Euros":
                jTextField2.setText(Double.toString(Conversor.dolaresAEuros(Integer.parseInt(jTextField1.getText()))));
                break;
                
            case"Metros/Pies":
                jTextField2.setText(Double.toString(Conversor.metrosAPies(Integer.parseInt(jTextField1.getText()))));
                break;
                
                
        }
        
    }//GEN-LAST:event_jTextField1ActionPerformed

