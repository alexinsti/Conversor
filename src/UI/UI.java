    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        // TODO add your handling code here:
        switch((String)jComboBoxMedidas.getSelectedItem()){
            case"Km/Millas":
                jTextField1.setText(Double.toString(Conversor.millasAKm(Integer.parseInt(jTextField2.getText()))));
                break;
            
            case"L/Galones":
                jTextField1.setText(Double.toString(Conversor.galonesAL(Integer.parseInt(jTextField2.getText()))));
                break;
                
            case"Dólares/Euros":
                jTextField1.setText(Double.toString(Conversor.eurosADolares(Integer.parseInt(jTextField2.getText()))));
                break;
                
            case"Metros/Pies":
                jTextField1.setText(Double.toString(Conversor.piesAMetros(Integer.parseInt(jTextField2.getText()))));
                break;
                
                
        }
        
    }//GEN-LAST:event_jTextField2ActionPerformed

    public void cambiaLabels(String modo){
        switch(modo){
            case"Km/Millas":
                reiniciaTextFields();
                jLabel1.setText("Km");
                jLabel2.setText("Millas");
                break;
            
            case"L/Galones":
                reiniciaTextFields();
                jLabel1.setText("Litros");
                jLabel2.setText("Galones");
                break;
                
            case"Dólares/Euros":
                reiniciaTextFields();
                jLabel1.setText("Dólares");
                jLabel2.setText("Euros");
                break;
                
            case"Metros/Pies":
                reiniciaTextFields();
                jLabel1.setText("Metros");
                jLabel2.setText("Pies");
                break;
                
                
        }
    }
    public void reiniciaTextFields(){
        jTextField1.setText("0");
        jTextField2.setText("0");
    }
    
    
